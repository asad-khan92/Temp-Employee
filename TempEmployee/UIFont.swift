import Foundation
import UIKit

extension UIFont{
    
    open class func HalisRRegular(size:CGFloat) -> UIFont{
        
        return UIFont.init(name: "HalisR-Regular", size: size)!
    }
    
    open class func HalisRBold(size:CGFloat) -> UIFont{
        
        return UIFont.init(name: "HalisR-Bold", size: size)!
    }
   
}
